    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        if (jTxtCodigo.getText().trim().isEmpty()) {
            Util.mensagem("Pesquise uma Vendas antes de Excluir");
            return;
        }
        if (Util.perguntar("Deseja excluir ?")) {

            VendasDAO vendasDAO = new VendasDAO();
            VendasProdutosDAO vendasProdutosDAO = new VendasProdutosDAO();

            for (int i = 0; i < jTable1.getRowCount(); i++) {
                VmanVendasProdutos vp = controllerVenProd.getBean(i);
                vendasProdutosDAO.delete(vp);
            }

            vendasDAO.delete(viewBean());
        }

        Util.limpar(
                jTxtCodigo,
                jFmtData,
                jCboxCliente,
                jTxtTotal,
                jCboxVendedor
        );
        Util.habilitar(false, jBtnConfirmar, jBtnCancelar, jBtnAlterar, jBtnExcluir);
        Util.habilitar(true, jTxtCodigo, jFmtData, jTxtTotal, jCboxCliente, jCboxVendedor, jBtnIncluir, jBtnPesquisar);

        controllerVenProd.setList(new ArrayList<>());
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        VendasDAO vendasDAO = new VendasDAO();
        VendasProdutosDAO vendasProdutosDAO = new VendasProdutosDAO();
        VmanVendas vendas = viewBean();

        if (incluir) {
            vendasDAO.insert(vendas);
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                VmanVendasProdutos vp = controllerVenProd.getBean(i);
                vp.setVmanVendas(vendas);
                vendasProdutosDAO.insert(vp);
            }
        } else {
            vendasDAO.update(vendas);
            vendasProdutosDAO.deleteProdutos(vendas);
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                VmanVendasProdutos vp = controllerVenProd.getBean(i);
                vp.setVmanVendas(vendas);
                vendasProdutosDAO.insert(vp);
            }
        }

        Util.habilitar(
                false,
                jBtnAlterar1,
                jBtnExcluirProd,
                jBtnIncluirProd,
                jBtnConfirmar,
                jBtnCancelar,
                jTxtCodigo,
                jFmtData,
                jCboxCliente,
                jCboxVendedor
        );

// habilitar
        Util.habilitar(
                true,
                jBtnIncluir,
                jBtnExcluir,
                jBtnAlterar,
                jBtnPesquisar
        );

// limpar
        Util.limpar(
                jTxtCodigo,
                jFmtData,
                jCboxCliente,
                jTxtTotal,
                jCboxVendedor
        );

        controllerVenProd.setList(new ArrayList<>());
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

