    private void jTxtQuantidadeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtQuantidadeKeyReleased
        // TODO add your handling code here:
        if (jTxtQuantidade.getText().isEmpty() == false) {
            VmanProdutos produto = (VmanProdutos) jCbxVmanProd.getSelectedItem();
            int quant = Util.strToInt(jTxtQuantidade.getText());
            jTxtVmanTotal.setText(
                    Util.doubleToString(quant * produto.getVmanPreco())
            );
        } else {
            Util.limpar(jTxtVmanTotal);
        }
    }//GEN-LAST:event_jTxtQuantidadeKeyReleased

    private void jCbxVmanProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCbxVmanProdActionPerformed
        // TODO add your handling code here:
        VmanProdutos produto = (VmanProdutos) jCbxVmanProd.getSelectedItem();
        jTxtVmanVU.setText(Util.doubleToString(produto.getVmanPreco()));
        int quant = Util.strToInt(jTxtQuantidade.getText());
        jTxtVmanTotal.setText(
                Util.doubleToString(quant * produto.getVmanPreco())
        );
    }//GEN-LAST:event_jCbxVmanProdActionPerformed

