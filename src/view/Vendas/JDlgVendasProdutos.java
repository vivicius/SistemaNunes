public void setTelaAnterior(JDlgVendas jDlgVendas, VmanVendasProdutos vendasProd) {
        this.jDlgVendas = jDlgVendas;
        if (vendasProd != null) {
            incluir = false;
            jCbxVmanProd.setSelectedItem(vendasProd.getVmanProdutos());
            jTxtQuantidade.setText(Util.intToString(vendasProd.getVmanQuantidade()));
        } else {
            incluir = true;
        }
    }
    private void jBtnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOKActionPerformed
        // TODO add your handling code here:
        VmanVendasProdutos vendasProdutos = new VmanVendasProdutos();
        vendasProdutos.setVmanProdutos((VmanProdutos) jCbxVmanProd.getSelectedItem());
        vendasProdutos.setVmanQuantidade(Util.strToInt(jTxtQuantidade.getText()));
        vendasProdutos.setVmanValorUnitario(Util.strToDouble(jTxtVmanVU.getText()));
       
        if (incluir == true) {
            jDlgVendas.controllerVenProd.addBean(vendasProdutos);
        } else {
            jDlgVendas.controllerVenProd.removeBean(jDlgVendas.getjTable1().getSelectedRow());
            jDlgVendas.controllerVenProd.addBean(vendasProdutos);
        }
        setVisible(false);
        
    }//GEN-LAST:event_jBtnOKActionPerformed

